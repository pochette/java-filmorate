CREATE TABLE IF NOT EXISTS USERS
(
    USER_ID  INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME     VARCHAR(255) NOT NULL,
    EMAIL    VARCHAR(255) NOT NULL UNIQUE,
    LOGIN    VARCHAR(255) NOT NULL UNIQUE,
    BIRTHDAY DATE         NOT NULL
);

CREATE TABLE IF NOT EXISTS GENRE_TYPE
(
    GENRE_ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME     VARCHAR(50) NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS MPA_TYPE
(
    MPA_ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MPA_NAME          VARCHAR(50) NOT NULL
);

CREATE TABLE IF NOT EXISTS FILMS
(
    FILM_ID      INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
    NAME         VARCHAR(255)                                         NOT NULL,
    DESCRIPTION  VARCHAR(1000)                                        NOT NULL,
    RELEASE_DATE DATE                                                 NOT NULL,
    DURATION     INTEGER                                              NOT NULL,
    MPA_ID       INTEGER
        REFERENCES MPA_TYPE (MPA_ID) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS FILM_GENRES
(
    FILM_ID  INTEGER REFERENCES FILMS (FILM_ID) ON DELETE CASCADE       NOT NULL,
    GENRE_ID INTEGER REFERENCES GENRE_TYPE (GENRE_ID) On DELETE CASCADE NOT NULL,
    PRIMARY KEY (FILM_ID, GENRE_ID)
);

CREATE TABLE IF NOT EXISTS FRIENDS
(
    USER_ID   INTEGER REFERENCES USERS (USER_ID) On DELETE CASCADE NOT NULL,
    FRIEND_ID INTEGER REFERENCES USERS (USER_ID) ON DELETE CASCADE NOT NULL,
    PRIMARY KEY (USER_ID, FRIEND_ID)
);

CREATE TABLE IF NOT EXISTS FILMS_LIKES
(
    LIKE_ID INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    USER_ID INTEGER REFERENCES USERS (USER_ID) ON DELETE CASCADE NOT NULL,
    FILM_ID INTEGER REFERENCES FILMS (FILM_ID) ON DELETE CASCADE NOT NULL,
    PRIMARY KEY (USER_ID, FILM_ID)
);

